    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String login = user.getText();
        char[] passwordArray = password_input.getPassword();
        String password = new String(passwordArray);
        if (login.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe um login v치lido!");
            return;
        }
        if (password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe uma senha v치lida!");
            return;
        }
        LoginController loginController = new LoginControllerFactory().build();
        boolean result;
        try {
            result = loginController.authenticate(login, password);
            if (!result) {
                JOptionPane.showMessageDialog(null, "Dados de acesso inv치lidos!");
                return;
            }
            JOptionPane.showMessageDialog(null, "Acesso Autorizado");
     
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }


    }//GEN-LAST:event_jButton1ActionPerformed

